myTuple = (1,2,3)
myList = [1,2,3]

#Identify the type of value

print(type(myTuple))
print(type(myList))

#Identify the length of value
print(len(myTuple))

#Identify the Index Value
print(myTuple[0])

#Identify the Last Index Value
print(myTuple[-1])

#Built-in Methods
#count
myTupleCount = ('a', 'b', 'c', 'a')
print(myTupleCount.count('a'))

#index
myTupleIndex = ('a', 'b', 'c', 'a')
print(myTupleIndex.index('b'))

